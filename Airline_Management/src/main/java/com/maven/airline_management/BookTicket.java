    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String custm_id = customer_id.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root", "root");

                // prepare statement first
                ps = con.prepareStatement("select * from customers where customer_id=?");

                // now set values for each parameter
                ps.setString(1, custm_id);

                // execute query 
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    String name = rs.getString("name");
                    String pass_id = rs.getString("passport_id");
                    String nic_id = rs.getString("nic_id");
                    String uaddress = rs.getString("address");
                    String gender = rs.getString("gender");
                    String ucontact = rs.getString("contact");

                    String dob = rs.getString("dob");
                    try {
                        Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dob);
                        this.dob.setDate(date);

                    } catch (ParseException ex) {
                        Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    fname.setText(name);
                    passport.setText(pass_id);
                    nic.setText(nic_id);
                    address.setText(uaddress);
                    if ("male".equals(gender)) {
                        male.setSelected(true);
                    } else {
                        female.setSelected(true);
                    }

                    this.contact.setText(ucontact);

                } else {
                    JOptionPane.showMessageDialog(null, "Customer doesnot exist");
                    customer_id.setText("");
                }

            } catch (SQLException ex) {
                Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_addActionPerformed

