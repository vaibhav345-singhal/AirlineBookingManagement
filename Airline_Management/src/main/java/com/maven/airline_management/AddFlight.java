    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String f_id = flight_id.getText();
        String f_arrival = flight_arrival.getText();
        String f_departure = flight_departure.getText();
        String name = flight_name.getText();
        String seats = flight_ticket.getText();
        String f_fare = this.fare.getText();

        DateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
        String f_date = dt.format(flight_date.getDate());

        try {
            Class.forName("com.mysql.jdbc.Driver");
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root", "root");
                // prepare statement first
                ps = con.prepareStatement("insert into flights set flight_id=?, arrival = ? , departure = ?, seats=?,fare=?, flight_date=?,flight_name=?");

                // now set values for each parameter
                ps.setString(1, f_id);
                ps.setString(2, f_arrival);
                ps.setString(3, f_departure);
                ps.setString(4, seats);
                ps.setString(5, f_fare);
                ps.setString(6, f_date);
                ps.setString(7, name);

                // execute query 
                ps.executeUpdate();

                // show alert 
                JOptionPane.showMessageDialog(null, "FLIGHT IS ADDED");
            } catch (SQLException ex) {
                Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


