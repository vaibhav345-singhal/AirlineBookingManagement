    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String farrival = arrival.getSelectedItem().toString();
        String fdeparture = departure.getSelectedItem().toString();
        // for total count 
        int cc;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root", "root");
                ps = con.prepareStatement("select * from flights where arrival=? and departure=?");
                ps.setString(1, farrival);
                ps.setString(2, fdeparture);

                ResultSet rs = ps.executeQuery();

                ResultSetMetaData rdms = rs.getMetaData();
                cc = rdms.getColumnCount();
                // getting table model to add values in it
                DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
                dft.setRowCount(0);

                while (rs.next()) {
                    Vector v = new Vector();
                    for (int i = 1; i <= cc; i++) {
                        v.add(rs.getString("flight_id"));
                        v.add(rs.getString("flight_name"));
                        v.add(rs.getString("arrival"));
                        v.add(rs.getString("departure"));
                        v.add(rs.getString("flight_date"));
                        v.add(rs.getString("fare"));
                        v.add(rs.getString("seats"));

                    }
                    dft.addRow(v);
                }

            } catch (SQLException ex) {
                Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

