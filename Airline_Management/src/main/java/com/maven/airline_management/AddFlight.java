    public void generate_flight_id() {
        try {

            Class.forName("com.mysql.jdbc.Driver");
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root", "root");
                // creating a statment to execute query
                Statement s = con.createStatement();
                // here executing the query and storing result in resultset
                ResultSet rs = s.executeQuery("select max(flight_id) as max_id from flights");
                rs.next();
                if (rs.getString("max_id") == null) {
                    flight_id.setText("FL001");
                } else {
                    long id = Long.parseLong(rs.getString("max_id").substring(2));
                    id++;
                    flight_id.setText("FL" + String.format("%03d", id));
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddCustomers.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

